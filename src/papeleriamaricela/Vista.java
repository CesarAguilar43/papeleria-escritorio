    public void dinero() {
        double total = 0.0;
        tablaDat = (DefaultTableModel) tablaVentas.getModel();
        int tot = tablaDat.getRowCount();
        for (int i = 0; i < tot; i++) {
            total += Double.parseDouble(tablaVentas.getValueAt(i, 4).toString());
        }
        dinero += total;
        jLabel11.setText(String.valueOf(dinero));

    }

