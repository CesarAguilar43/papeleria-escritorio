    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int fila, colum, cont = 0;
        fila = tablaVentas.getRowCount();
        colum = tablaVentas.getColumnCount();
        tablaDestino = (DefaultTableModel) tablaVentas.getModel();
        try {
            if (fila == 0) {
                JOptionPane.showMessageDialog(null, "No hay datos para guardar", "Advertencia", JOptionPane.WARNING_MESSAGE);
            } else {
                int resp = JOptionPane.showConfirmDialog(null, "¬øDesea guardar la venta?", "Guardar", JOptionPane.YES_NO_OPTION);
                if (resp == JOptionPane.YES_NO_OPTION) {
                    datos = new Object[fila][colum];
                    for (int i = 0; i < fila; i++) {
                        for (int j = 0; j < colum; j++) {
                            datos[i][j] = tablaVentas.getValueAt(i, j);
                        }
                    }
                    m = (DefaultTableModel) jTable1.getModel();
                    for (Object[] dato : datos) {
                        m.addRow(dato);
                    }
                    JOptionPane.showMessageDialog(null, "Guardado correctamente", "Guardado", JOptionPane.INFORMATION_MESSAGE);
                    dinero();
                    double precioAc = Double.parseDouble(jTextField1.getText());
                    resultado -= precioAc;
                    jTextField1.setText(String.valueOf(resultado));
                    jTextField2.setText("");
                    jTextField3.setText("0.0");
                    borrarTabla();
                }
            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Ocurrio un error", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

